
function addNum(n1 , n2){
    let answer = n1 + n2
    return answer
}

function subNum(n1 , n2){
    let answer = n1 - n2
    return answer
}

function divNum(n1 , n2){
    let answer = n1 / n2
    return answer
}

function multNum(n1 , n2){
    let answer = n1 * n2
    return answer
}

console.log('Add : ', addNum(5, 10))
console.log('Sub : ', subNum(15, 10))
console.log('Div : ', divNum(10, 5))
console.log('Mult : ', multNum(5, 10))

